package org.example.exerciseModule4.model;

public class Reports {
    private int rebels = 0;
    private int traitors = 0;
    private int total;

    public int getRebels() {
        return rebels;
    }

    public void setRebels(int rebels) {
        this.rebels = rebels;
    }

    public int getTraitors() {
        return traitors;
    }

    public void setTraitors(int traitors) {
        this.traitors = traitors;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }
}
